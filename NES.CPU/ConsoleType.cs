namespace NES.CPU
{
    public enum ConsoleType
    {
        //          |||| ||++- Console type
        //          |||| ||     0: Nintendo Entertainment System/Family Computer
        NES = 0x0,
        //          |||| ||     1: Nintendo Vs. System
        VsSystem = 0x1,
        //          |||| ||     2: Nintendo Playchoice 10
        Playchoice = 0x2,
        //          |||| ||     3: Extended Console Type
        Extended = 0x3
    }
}